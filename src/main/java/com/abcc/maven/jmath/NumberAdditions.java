package com.abcc.maven.jmath;

public class NumberAdditions {
	
	public static long add2numbers (int iNum1, int iNum2) {
	
		long iResult = iNum1 + iNum2;
				
	return iResult;
	}
	
	public static long add3numbers (int iNum1, int iNum2, int iNum3) {
		
		long	iResult = iNum1 + iNum2 + iNum3;
				
	return iResult;
	}

}
